#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	decimal_to_hex(unsigned char c )
{
	char	*base;

	base = "0123456789abcdef";
	ft_putchar(base[c / 16]);
	ft_putchar(base[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 32 && str[i] <= 126))
		{
			ft_putchar('\\');
			decimal_to_hex(str[i]);
		}
		else
			ft_putchar(str[i]);
		i++;
	}
}
